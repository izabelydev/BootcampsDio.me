// ARRAY - LISTA DE CONTEÚDO

//sintaxe
const alunos = ['João', 'Iza', 'Maria'];

console.log(alunos[0]); //na posição zero é o joão

alunos.push('Renan'); //adicionar item na lista de forma dinâmica

alunos[4] = 'Vinicius'; //adicionar item na posição da lista

alunos[1] = 'Izabely'; //sobrescreveer item na lista

console.log(alunos); //mostrar tudo na lista

console.log(alunos.pop()); //remover ultimo item da lista

console.log(alunos.pop.shift()); //remover primeiro item da lista

console.log(alunos.length); //saber o tamanho da lista
